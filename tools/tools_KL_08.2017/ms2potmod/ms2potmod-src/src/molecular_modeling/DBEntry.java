    /**
     * 
     * validate the double values when user move to the next control.
     * 
    */
    private void DoubleValFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_DoubleValFocusLost
        JTextField sf = (JTextField) evt.getSource();
        try{
            
            if(sf.getText() != null && !sf.getText().equalsIgnoreCase(""))
            {
                          double d =  Double.parseDouble(sf.getText());
                      if(Double.compare(d, 0.0) < 0.0)
                          {
                              this.setAlwaysOnTop(false);
                              JOptionPane.showMessageDialog(null, "Value can not be negative");
                              this.setAlwaysOnTop(true);
                              sf.requestFocus();
                              sf.selectAll();
                          }
            }
        }
        catch(Exception ex)
        {
            this.setAlwaysOnTop(false);
            JOptionPane.showMessageDialog(null, "Only number values are allowed/wrong seperator between number values");
            this.setAlwaysOnTop(true);
            sf.requestFocus();
            sf.selectAll();
        }
    }//GEN-LAST:event_DoubleValFocusLost
    
