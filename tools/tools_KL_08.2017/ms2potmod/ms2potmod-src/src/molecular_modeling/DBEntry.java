    private void sitetypetxtFocusGained(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_sitetypetxtFocusGained
        JTextField sf = (JTextField) evt.getSource();
        if(sf.getText() != null && !sf.getText().equals(""))
        {
            if(sf.getText().startsWith("'"))
                         sf.setText(sf.getText().substring(1));
            
            if(sf.getText().endsWith("'"))
                sf.setText(sf.getText().substring(0, sf.getText().length() - 1));
        }
    }//GEN-LAST:event_sitetypetxtFocusGained

