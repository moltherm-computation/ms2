    /**
     *
     * @see DBEntry#SaveBtnActionPerformed(java.awt.event.ActionEvent)
     * 
     * This function then formates all the provided values for hashmap 
     * (t_val) in to the specified database formate and write these 
     * formated values to the database file.
     * 
     * @param segment Segment Which to save data
     * 
     */
    private void writeTypesinDB(String segment)
    {
        String filepath = UserDirectory+""+File.separator+"Database"+File.separator+"potparam.txt";
        FileInputStream in = null;
        Scanner sc = null;
        BufferedWriter wr = null;
        try {
            
        in = new FileInputStream(filepath);
        sc = new Scanner(in,"UTF-8");
        StringBuffer content = new StringBuffer();
        String line = null;
        while(sc.hasNextLine()){
            line = sc.nextLine();
            content.append(line+"\n");
            
            if(line.contains(segment))
                               break;
                    }
        
        while(sc.hasNextLine())
        {
            line = sc.nextLine();
            content.append(line+"\n");
            
        if(line.contains("/"))
                    break; 
        }
        
        //ListIterator iterate =  (ListIterator) (types.entrySet()).iterator();
        ListIterator<Map.Entry<String, String>> iterator = new ArrayList<Map.Entry<String, String>>(t_val.entrySet()).listIterator(t_val.size());
        content.append("\n"+segment+"\n");
        
        while(iterator.hasPrevious())
        {
            Map.Entry entry = (Map.Entry) iterator.previous();
            //line = "   "+entry.getKey()+"\t= "+entry.getValue();
            line = String.format("   %-17s= %s", entry.getKey(),entry.getValue());
            content.append(line+"\n");
        }
        
        if(segment.equals("&torsion"))
        {
           if(getComponents(DihedPanelT))
           {
            Iterator itr =  t_val.entrySet().iterator();
            while(itr.hasNext())
            {
                Map.Entry entry = (Map.Entry) itr.next();
               // line = "   "+entry.getKey()+"\t= "+entry.getValue();
                line = String.format("   %-17s= %s", entry.getKey(),entry.getValue());
                content.append(line+"\n");
            }
           }
        }
        
            line = "/\n";
            content.append(line+"\n");
            
        while(sc.hasNextLine())
        {
            line = sc.nextLine();
            content.append(line+"\n");
        }
        
        
        wr = new BufferedWriter(new FileWriter(filepath));
                   wr.append(content);

        System.out.println("all done!\n");
        segment = segment.substring(1);
        statuslbl.setText(segment+" entry saved successfully!");
        
        } catch (FileNotFoundException ex) {
            Logger.getLogger(DBEntry.class.getName()).log(Level.SEVERE, null, ex);
            statuslbl.setText("An exception occurred. Try again");
        }
        catch (Exception ex)
        {
            Logger.getLogger(DBEntry.class.getName()).log(Level.SEVERE, null, ex);
            statuslbl.setText("An exception occurred. Try again");
        }
        finally{ 
            try {
                    if (in != null) {
                            in.close();
                }
                    if (sc != null) {
                        sc.close();
                    }
                 if (wr != null) {
                        wr.close();
                    }
            } catch (IOException ex) {
                Logger.getLogger(DBEntry.class.getName()).log(Level.SEVERE, null, ex);
                statuslbl.setText("An exception occurred. Try again");
            }
        }
    }
    
