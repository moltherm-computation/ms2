    public class etaxiTableCellRenderer extends JPanel implements TableCellRenderer {
    
        public Component getTableCellRendererComponent(JTable table, Object value,
                boolean isSelected, boolean hasFocus, int row, int col) {
            JPanel panel = null;
            if (col >= row+1) {
                EnsemblePanel curE = (EnsemblePanel) FileGenGUI.ensemblesTabbedPane.getSelectedComponent();

                PotentialModel rowPm = curE.eData.potModelList.get(row);
                PotentialModel colPm = curE.eData.potModelList.get(col);
                
                EtaXi cur = curE.eData.getEtaxi();
                
                while (cur.getNext()!=null){
                    if (cur.getPm1()==rowPm && cur.getPm2()==colPm)
                       break; 
                    cur = cur.getNext();
                }
                
                panel = new EtaXiJPanel(table, cur);

            } else {
                panel = null;
                //setBackground(200,200,200);
            }
            if (col==0){}

            return panel;
        }
 
    }
    
