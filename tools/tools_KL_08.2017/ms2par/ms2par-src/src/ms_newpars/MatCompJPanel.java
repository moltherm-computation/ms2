    public void updateEtaXiTable(){
                           
         EnsemblePanel curE = (EnsemblePanel) FileGenGUI.ensemblesTabbedPane.getSelectedComponent();
         
         int pmNum = curE.eData.potModelList.size();
         Object [][] TableObject = new Object[pmNum-1][pmNum-1];
         String [] Header = new String[pmNum];
         Header[0] = "Pot.Model";
         
         for (int i=1; i<pmNum;i++){
             PotentialModel pm = curE.eData.potModelList.get(i);
             Header[i] = pm.getName().toString();
             
         }
         for (int i=0; i<pmNum-1;i++){
             PotentialModel pm = curE.eData.potModelList.get(i);
             TableObject[i][0] = pm.getName().toString(); 
         }
                
         
         jTable1.setModel(new javax.swing.table.DefaultTableModel(TableObject,Header));
         this.jTable1.setDefaultRenderer(Object.class, new etaxiTableCellRenderer() );
         this.jTable1.setDefaultEditor(Object.class, new etaxiTableCellEditor() );
         this.jTable1.getColumnModel().getColumn(0).setCellRenderer( jTable1.getTableHeader().getDefaultRenderer() );
         this.jTable1.setRowHeight(75);
     }
     
