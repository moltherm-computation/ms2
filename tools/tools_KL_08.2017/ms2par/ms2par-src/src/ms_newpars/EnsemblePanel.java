    /**
     * Set filters for potential model files
     * Populate potential model list by selected file and call potential model class.
     * 
     * @param evt 
     */
    private void potmodeladdButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_potmodeladdButtonActionPerformed
        int PmTabRows;
        
        String pmdirpathSelected;
        File pmfileSelected;
        File pmdirSelected;
        File pmdirDefault = null;
        
        class pmFilter extends FileFilter {
            public String getDescription() {
                return "Only Potential Model Files";
            }
            
            public boolean accept(File f) {
                if (f == null)
                    return false;
                if (f.isDirectory())
                    return true;
                return f.getName().toLowerCase().endsWith(".pm");
            }
        }
        
        JFileChooser pmfileChooser = new JFileChooser();
        pmfileChooser.addChoosableFileFilter(new pmFilter());
        pmfileChooser.setFileSelectionMode(JFileChooser.FILES_ONLY);
        pmfileChooser.setCurrentDirectory(pmdirDefault);
        int returnVal = pmfileChooser.showOpenDialog(this);
        if (returnVal == JFileChooser.APPROVE_OPTION) {
            pmdirSelected = pmfileChooser.getCurrentDirectory();
            pmfileSelected = pmfileChooser.getSelectedFile();
            pmdirpathSelected = pmdirSelected.getAbsolutePath();
            
            double molfrac=1.0;
            String perm = "No";
            if (this.eData.potModelList.size() > 0){
                
                Iterator pmIt = this.eData.potModelList.iterator();
                while (pmIt.hasNext()) {
                    PotentialModel pm = (PotentialModel)pmIt.next();
                    molfrac -= pm.getMolarFrac();
                    
                }
            }
            
            molfrac = Math.round(molfrac*10.)/10.;
            double input = -1.0;
            while (input < 0.0 || input > 1.0) {
                try {
                    input = this.main.validateInputDouble(JOptionPane.showInputDialog("Mole Fraction:",molfrac));
                } catch(Exception e) {
                    break;
                }
            }
            molfrac = input;
            
            if (input >= 0.0 && input <= 1.0) {
                PotentialModel pm = new PotentialModel(pmfileSelected, molfrac,"Yes");
                this.eData.potModelList.add(pm);
                
                if (this.eData.potModelList.size() > 1){
                    addEtaXi(pm);
                    
                }
            }
            
            updatePmTable();
            this.numberofcomponentsLabel.setText(String.valueOf(this.potmodelTable.getRowCount()));
            updatePmButtons();
            
        }
        
    }//GEN-LAST:event_potmodeladdButtonActionPerformed

