    /**
     * Calculating the value of maximum cutOff depending on the system of units and
     * the value of the density.
     */
    private void calcMaxCutoff(){
        
        if (this.main.gData.getLengthUnit()==0.0)
            JOptionPane.showMessageDialog(new JFrame()," LenghtUnit = 0.0 ");
        else if (this.eData.getNPart()==0)
            JOptionPane.showMessageDialog(new JFrame()," NParticles = 0 ");
        else if (this.eData.getDensity()==0.0)
            JOptionPane.showMessageDialog(new JFrame()," Density = 0.0 ");
        else {
                
            double maxCutoff;
            double density = this.eData.getDensity();
            if ((this.main.systemofunitsComboBox.getSelectedItem().toString().equals("SI"))){
                density = (density*1E-27)*(6.022E23)*(Math.pow(this.main.gData.getLengthUnit(),3.));
      
            }
            maxCutoff = (this.eData.getNPart())/density;

            maxCutoff = 0.5 * Math.pow(maxCutoff, 1./3.) ;
            maxCutoff = Math.round(maxCutoff*100.)/100.;
 
                    
            if (this.main.typeofensembleComboBox.getSelectedItem().toString().equals("NVE") ||this.main.typeofensembleComboBox.getSelectedItem().toString().equals("NVT")){
                    this.eData.setCutOff(maxCutoff);
                    
                    this.eData.setCutOffLJ(maxCutoff);
                    this.eData.setCutOffDD(maxCutoff);
                    this.eData.setCutOffDQ(maxCutoff);
                    this.eData.setCutOffQQ(maxCutoff);
                    if (this.main.cutofftypeComboBox.getSelectedItem().toString().equals("COM")){
                        
                        this.cutoffTextField.setText(String.valueOf(this.eData.getCutOff()));
                    }                     
                    else{
                        
                        this.cutoffljTextField.setText(String.valueOf(this.eData.getCutOffLJ()));
                        this.cutoffddTextField.setText(String.valueOf(this.eData.getCutOffDD()));
                        this.cutoffdqTextField.setText(String.valueOf(this.eData.getCutOffDQ()));
                        this.cutoffqqTextField.setText(String.valueOf(this.eData.getCutOffQQ()));
                    }                      
            }
            else{
                FileGenGUI parent2 = this.main;
                Ensemble parent1 = this.eData;          
                this.eData.setCutOff(maxCutoff);
               
                this.cutoffTextField.setEnabled(false);
                this.jLabel24.setEnabled(false);
                new changeMaxCutoffJFrame(parent1,this,parent2).setVisible(true);
           }
 
        }
        
    }
    

    
