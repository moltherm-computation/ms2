    private void criteriaspinnerStateChanged(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_criteriaspinnerStateChanged
        int crt = Integer.parseInt(criteriaspinner.getValue().toString());
        if(this.criteria.size() >= crt ) // numofcriteria
        {
            criteriaspinnerlbl.setText("Saved Criteria:");
            int count = 0;
            for(Component cmp: this.hbondcriteriapanel.getComponents())
            {
                if(cmp instanceof JTextField)
                {
                    JTextField field = (JTextField) cmp;
                    field.setText(criteria.get(crt-1).get(count).toString());
                    count++;
                 }
            }
            
            this.delCriteriabtn.setEnabled(true);
        }
        else
            setdefaultValue();
            
 
    }//GEN-LAST:event_criteriaspinnerStateChanged
    private void setdefaultValue() {
            this.delCriteriabtn.setEnabled(false);
            criteriaspinnerlbl.setText("Add Criteria:");
            int count = 0;
            for(Component cmp: this.hbondcriteriapanel.getComponents())
            {
                if(cmp instanceof JTextField)
                {
                    JTextField field = (JTextField) cmp;
                    field.setText(dval[count]);
                    count++;
                 }
            }
    }
    private void IntegerValidationOnFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_IntegerValidationOnFocusLost
        JTextField sf = (JTextField) evt.getSource();
        try{
            
            int d =  Integer.parseInt(sf.getText());
            
        }
        catch(Exception ex)
        {
            this.statuslbl.setText("Only number values are allowed");
            sf.requestFocus();
            sf.selectAll();
        }
    }//GEN-LAST:event_IntegerValidationOnFocusLost

