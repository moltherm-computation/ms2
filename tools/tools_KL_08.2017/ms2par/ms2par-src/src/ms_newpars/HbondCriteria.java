    private void HbondsavebtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_HbondsavebtnActionPerformed
      int crt = Integer.parseInt(criteriaspinner.getValue().toString());
        if(numofcriteria < crt)
      {
        criteria.add(numofcriteria,new ArrayList<Object>(9));
        ArrayList<Double> dl= new ArrayList<Double>();
         for(Component cmp: this.hbondcriteriapanel.getComponents())
         {
             if(cmp instanceof JTextField)
             {
                 JTextField field = (JTextField) cmp;
                 if(field.getName() != null && field.getName().equals("Double"))
                     //criteria.get(numofcriteria).add(Double.parseDouble(field.getText()));
                     dl.add(Double.parseDouble(field.getText()));
                 else
                    criteria.get(numofcriteria).add(Integer.parseInt(field.getText()));      
             }
         }
        criteria.get(numofcriteria).addAll(dl);
        numofcriteria++;
        criteriaspinner.setModel(new SpinnerNumberModel(1,1,numofcriteria+1,1));
        criteriaspinner.setValue(numofcriteria+1);
        ((DefaultEditor) criteriaspinner.getEditor()).getTextField().setEditable(false);

        this.statuslbl.setText("saved criteria : "+numofcriteria);
         //criteriaspinnerlbl.setText("Saved Criteria:");
      }
        else{
                int count = 0;
            ArrayList<Double> dl= new ArrayList<Double>();
         for(Component cmp: this.hbondcriteriapanel.getComponents())
         {
             if(cmp instanceof JTextField)
             {
                 JTextField field = (JTextField) cmp;
                 if(field.getName() != null && field.getName().equals("Double"))
                     //criteria.get(numofcriteria).add(Double.parseDouble(field.getText()));
                     dl.add(Double.parseDouble(field.getText()));
                 else
                 {
                    criteria.get(crt-1).set(count,Integer.parseInt(field.getText()));
                    count++;
                 }
             }
             
         }
         for(int i=0;i<dl.size();i++)
         {
            criteria.get(crt-1).set(count,dl.get(i));
            count++;
         }
         
         this.statuslbl.setText("updated criteria : "+crt);
        }
     
         
    }//GEN-LAST:event_HbondsavebtnActionPerformed
   
