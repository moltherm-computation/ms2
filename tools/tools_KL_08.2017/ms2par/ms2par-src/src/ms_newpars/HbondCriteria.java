    private void delCriteriabtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_delCriteriabtnActionPerformed
        
        int index = Integer.parseInt(this.criteriaspinner.getValue().toString());
        if(this.criteria.size() > 0) {
            this.criteria.remove(index - 1);
            setcriteriaspinner();
            this.statuslbl.setText("Deleted Criteria : "+index);
            setdefaultValue();
        }
        else {
             this.statuslbl.setText("Not saved yet...");
        }
        
    }//GEN-LAST:event_delCriteriabtnActionPerformed

