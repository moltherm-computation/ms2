    public void updatePmTable() {
        if (this.eData.potModelList.size() > 0){
            
            Object[][] pmObjArray = new Object [this.eData.potModelList.size()][2];
            
            for (int i=0; i < pmObjArray.length; i++) {
                
                PotentialModel curPm = this.eData.potModelList.get(i);
                
                pmObjArray[i][0] = curPm.getName();
                pmObjArray[i][1] = curPm.getMolarFrac();
            }
            potmodelTable.setModel(new javax.swing.table.DefaultTableModel(
                    (Object[][]) pmObjArray ,
                    new String [] {"Pot. Model", "Mole Fract."}
            )
            );
            
        } else {
            initPmTable();
        }
        this.potmodelTable.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        this.potmodelTable.getColumnModel().getColumn(0).setCellEditor(new NoTableCellEditor());
        this.potmodelTable.getColumnModel().getColumn(1).setCellEditor(new pmTableCellEditor());
        this.potmodelTable.getColumnModel().getColumn(0).setCellRenderer( this.potmodelTable.getTableHeader().getDefaultRenderer() );
        this.numberofcomponentsLabel.setText(String.valueOf(this.potmodelTable.getRowCount()));
        
    }
    
