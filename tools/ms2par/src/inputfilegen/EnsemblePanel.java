    public void addEtaXi(PotentialModel pm) {
        LinkedList pmList = this.eData.potModelList;
        
        /* Insert first EtaXi for 2nd Model */
        if (this.eData.getEtaxi() == null){
            this.eData.setEtaxi(new EtaXi((PotentialModel) pmList.get(0),pm));
            this.eData.setLast(this.eData.getEtaxi());
        } else {
            EtaXi cur = this.eData.getEtaxi();
            int i = 0;
            while (cur != this.eData.getLast()){
                // Insert EtaXi at row end 
                if (cur.getRowEnd()){
                    EtaXi newEtaXi = new EtaXi((PotentialModel) pmList.get(i),pm);
                    newEtaXi.setNext(cur.getNext());
                    cur.setNext(newEtaXi);
                    cur.setRowEnd(false);
                    cur = cur.getNext();
                    i++;
                }
                
                cur = cur.getNext();
            }
            cur.setRowEnd(false);
            /* Add EtaXi after last Element*/
            cur.setNext(new EtaXi((PotentialModel) pmList.get(i),pm));
            this.eData.setLast(this.eData.getLast().getNext());
            i++;
            /* Add last EtaXi */
            this.eData.getLast().setNext(new EtaXi((PotentialModel) pmList.get(i),pm));
            this.eData.setLast(this.eData.getLast().getNext());
            
        }
    }
    
    private void removeEtaXi(PotentialModel pm){
        
        EtaXi cur = this.eData.getEtaxi();
        EtaXi prev = null;
        while(cur !=null){
            if (cur.getPm1()==pm || cur.getPm2()==pm){
                if (cur == this.eData.getEtaxi()){
                    this.eData.setEtaxi(cur.getNext());
                } else if (cur==this.eData.getLast()){
                    this.eData.setLast(prev);
                    prev.setNext(null);
                } else{
                    prev.setNext(cur.getNext());
                }
            }
            prev = cur;
            cur = cur.getNext();
        }
        
    }
    
    
